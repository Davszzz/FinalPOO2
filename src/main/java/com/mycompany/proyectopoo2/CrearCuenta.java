   StringBuilder camposIncorrectos = new StringBuilder();
   public void ValidarLogin()
   {
       camposIncorrectos.setLength(0);
       String pas = new String(txtnpas.getPassword());
       String pas2 = new String(txtnpas2.getPassword());
       
       
       if(txtnUsu.getText().isEmpty())
       {
            camposIncorrectos.append("- Usuario\n");
            labelusuario.setText("Campo obligatorio");
       }
       else
       {
           labelusuario.setText("✔");
       }
       
       if(txtncorreo.getText().isEmpty())
       {
           camposIncorrectos.append("- Correo\n");
           labelcorreo.setText("Campo obligatorio");
       }
       else if(!txtncorreo.getText().contains("@") || !txtncorreo.getText().contains(".com"))
       {
           camposIncorrectos.append("- Correo\n");
           labelcorreo.setText("Correo invalido");
       }
       else
       {
            labelcorreo.setText("✔");   
       }
       if(pas.isEmpty())
       {
           camposIncorrectos.append("- Contrasena\n");
           labelpas.setText("Campo obligatorio");
       }
       else
       {
           labelpas.setText("✔");
       }
       if(!pas.equals(pas2) || pas.isEmpty())
       {
           camposIncorrectos.append("- Contrasena\n");
           labelpas2.setText("No coinciden");
       }
       else
       {
           labelpas2.setText("✔");
       }
       if(txttelefono.getText().isEmpty())
       {
           camposIncorrectos.append("- Telefono\n");
           labeltel.setText("Campo obligatorio");
       }
       else if(txttelefono.getText().length()<9)
       {
           camposIncorrectos.append("- Telefono\n");
           labeltel.setText("Numero de telefono invalido");
       }
       else
       {
           labeltel.setText("✔");
       }
       if(txtdni.getText().isEmpty())
        {   
            camposIncorrectos.append("- DNI\n");
            labeldni.setText("Campo obligatorio");
        }
       else if(txtdni.getText().length()<8)
       {
           labeldni.setText("DNI Invalido");
       }
       else
       {
           labeldni.setText("✔");
       }
       if(comboG.getSelectedIndex()==0)
       {
           camposIncorrectos.append("- Genero\n");
           labelg.setText("Campo obligatorio");
       }
       else
       {
           labelg.setText("✔");
       }
   }
    

