    private void btnsignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsignActionPerformed
               // TODO add your handling code here:
        String pas = new String(txtnpas.getPassword());
        String pas2 = new String(txtnpas2.getPassword());
        ValidarLogin();
        
         if(txtnUsu.getText().isEmpty() || txtncorreo.getText().isEmpty() || pas.isEmpty() || pas2.isEmpty() || !pas2.equals(pas) || !txtncorreo.getText().contains("@") || !txtncorreo.getText().contains(".com") || txttelefono.getText().isEmpty() || txtdni.getText().isEmpty() || comboG.getSelectedIndex()==0 || txttelefono.getText().length()<9 || txtdni.getText().length()<8)
         {
            String mensaje = "Debe completar los siguientes campos correctamente:\n" + camposIncorrectos.toString();
            JOptionPane.showMessageDialog(null, mensaje, "Campos incorrectos", JOptionPane.WARNING_MESSAGE);
            
            
        }
        else
        {
            usuario usu = new usuario();
            usu.setTipo(1);
            usu.setUsuario(this.txtnUsu.getText());
            usu.setCorreo(this.txtncorreo.getText());
            usu.setContraseña(pas);
            usu.setTelefono(this.txttelefono.getText());
            usu.setDni(this.txtdni.getText());
            usu.setGenero(this.comboG.getSelectedItem().toString());
            uController.Añadir(usu); //Pasa el objeto con los datos escritos en el formulario/
            JOptionPane.showMessageDialog(null, "Gracias por regitrarse, se le rediccionara al inicio de sesion", "Redireccionamiento",JOptionPane.INFORMATION_MESSAGE);
            InicioSesion is = new InicioSesion();
            is.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_btnsignActionPerformed

